#include <bits/stdc++.h>
using namespace std;
#define int long long


int a[110];
void solve()
{
    int n;
    cin>>n;
    for(int i=1;i<=n;i++) cin>>a[i];
    sort(a+1,a+1+n);

    int ans=0;
    for(int i=1;i<=n;i++)
    {
        if(a[i]>=(n+1-i))
        {
            ans=max(ans,n+1-i);
        }
    }

    cout<<ans<<"\n";
}
signed main() 
{
    ios::sync_with_stdio(0);
    cin.tie(0);cout.tie(0);
    int T=1;
    // cin>>T;
    while(T--) solve();
    return 0;
}